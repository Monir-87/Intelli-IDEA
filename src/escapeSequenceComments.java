public class escapeSequenceComments {
    public static void main(String[] args) {
        System.out.print("Learn\tCar\n\tDriving\nEasy\tWay\n\n");
        System.out.print("\tLearn Car Driving \"Easy\" Way!\n");
        System.out.print( "Hello\n" );
        System.out.print( "Jhonni\by\n" );
        System.out.println( "Hard to believe, eh?" );
        System.out.println( "Surprised? /* abcde */ Or what did you expect?" );
        System.out.println( "\\ // -=- \\ //" );
        System.out.println( "\\\\ \\\\\\ \\\\\\\\" );
        System.out.print( "I hope you understand \"escape sequences\" now.\n" );

    }
}
